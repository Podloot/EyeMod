package com.podloot.eyemod.lib.gui.util;

public class Space {
	
	public int x, y, width, height;
	
	public Space(int x, int y, int width, int height) {
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}
	
	public Space(int width, int height) {
		this.width = width;
		this.height = height;
	}

}
